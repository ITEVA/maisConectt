        <h1 id="titulo">A EMPRESA</h1>
        <p>
            A MAIS CONECTT iniciou suas operações em 2010 em Fortaleza-ce
            no bairro Vila União. A decisão de fundar a empresa aconteceu
            depois que seu fundador viu à necessidade de atender a população
            de baixa renda, e com o crescimento das grandes operadoras de internet
            na cidade, que utilizavam como meio de acesso, as linhas telefônicas.
            Estas eram pouco acessíveis e tinham alto custo na utilização.
        </p>

        <p>
            Também estava claro que em pequenos setores do bairro, onde tudo
            começou praticamente não havia disponibilidade de linhas telefônicas,
            e esse serviço seria economicamente inviável, na visão de qualquer operadora.
            Foi neste momento que a empresa apostou na viabilidade de atender os moradores
            do bairro, mas para isso teria que vencer vários desafios: buscar uma tecnologia
            de baixo custo, de rápida implementação e funcional.
        </p>

        <p>
            Buscava-se uma solução tecnológica de baixo custo, chegando-se ao rádio.
            Naquele momento havia sido desenvolvido para distâncias de poucos metros e estava
            sendo utilizado para uso interno em pequenas redes corporativas fora do país.
            Houve a necessidade de fabricar grandes antenas para alcançar distâncias de quase
            uma centena de quilômetros, pois não havia este tipo de material no mercado.
        </p>

        <p id="ultimo">
            Decidiu-se desenvolver e fabricar as torres, bem como investir no desenvolvimento de
            todos os softwares, controles de rede e gestão, baseados na plataforma Linux. Foram fabricados
            diversos equipamentos eletrônicos e realizadas várias adaptações ate conseguir atender uma boa
            parte na rede de cabeamento misto onde chega fibra nos poste e atender no cabo metálico UTP.
        </p>

